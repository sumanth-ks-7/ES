								
  AREA RESET ,CODE,READONLY
  ENTRY
START
  MOV R5,#0x40000000 
  MOV R7,#0x4000001C
  LDR R0 , [R5];
  MOV R1,R0 ;
  CMP R0, #02;
  MOV R2, #01;
  BLT RESULT;
REPEAT
  SUBS R1,R1,#01;
  CMP R1,#01;
  BEQ STOP;
  MUL R2,R0,R1;
  MOV R0,R2;
  B REPEAT
RESULT MOV R0,R2;
  STR R0,[R7]
STOP B STOP
  END
  
